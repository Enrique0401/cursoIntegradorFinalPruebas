    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String nuevaDescripcion = jTextArea1.getText().trim();
            int cantidad = nuevaDescripcion.length();

            // A. Validaciones antes de conectar a la BD
            if (cantidad == 0) {
                JOptionPane.showMessageDialog(this, "⚠️ El campo no puede estar vacío.", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

            if (cantidad > 150) {
                JOptionPane.showMessageDialog(this, "⚠️ Has excedido el límite de 150 caracteres.", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // B. Guardar cambios
            ProyectoRepositorio repositorio = new ProyectoRepositorio();
            ProyectoService proyServ = new ProyectoService(repositorio);
            Model.Proyectos proyecto = proyServ.buscarPorId(this.idProyecto);

            if (proyecto != null) {
                proyecto.setNombre(nuevaDescripcion);

                boolean actualizado = proyServ.actualizar(proyecto);

                if (actualizado) {
                    // C. Notificar a la tabla para que se actualice sola
                    EntidadObservableSingleton.getInstancia().notificarObservadores();

                    this.dispose(); // Cerrar ventana
                }
            } else {
                JOptionPane.showMessageDialog(this, "❌ Error: No se encontró el proyecto en la base de datos.");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error técnico: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

