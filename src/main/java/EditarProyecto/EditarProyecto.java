    public EditarProyecto(int idProyecto, String descripcionActual) {
        this.idProyecto = idProyecto;
        initComponents(); // Inicia componentes visuales

        this.setDefaultCloseOperation(javax.swing.WindowConstants.DISPOSE_ON_CLOSE);

        // 1. Validación de seguridad para el texto
        if (descripcionActual == null) {
            descripcionActual = "";
        }

        // 2. Configurar el área de texto
        jTextArea1.setText(descripcionActual);
        jTextArea1.setLineWrap(true);       // Salto de línea automático
        jTextArea1.setWrapStyleWord(true);  // No cortar palabras

        // 3. INICIALIZAR EL CONTADOR AL ABRIR (Tu corrección)
        // Muestra la cantidad actual inmediatamente
        int largoInicial = descripcionActual.length();
        lblContador.setText(largoInicial + "/150");

        // 4. EVENTO: Controlar escritura en tiempo real
        jTextArea1.addKeyListener(new java.awt.event.KeyAdapter() {

            @Override
            public void keyTyped(java.awt.event.KeyEvent evt) {
                // Si llega a 150, no deja escribir más
                if (jTextArea1.getText().length() >= 150) {
                    evt.consume(); // Bloquea la tecla
                    java.awt.Toolkit.getDefaultToolkit().beep(); // Sonido de aviso
                }
            }

            @Override
            public void keyReleased(java.awt.event.KeyEvent evt) {
                // Actualiza el contador cada vez que sueltas una tecla
                int cantidad = jTextArea1.getText().length();
                lblContador.setText(cantidad + "/150");
            }
        });
    }

